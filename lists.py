shopping_list = ['apples', 'oranges', 'grapes', 'mangoes']
print(shopping_list)

# adding items to the list

shopping_list.append('lemon')
print(shopping_list)

# removes item from the list

shopping_list.remove('oranges')
print(shopping_list)

# removes last item in the list

shopping_list.pop()
print(shopping_list)

# returns length of the list
print(len(shopping_list))

# getting a value from a list
print(shopping_list[0])

# getting last value from a list
print(shopping_list[-1])

# slicing a list
print(shopping_list[0:2])
print(shopping_list[1:3])
